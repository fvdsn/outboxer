func TestLoadInitConfigParsesApplyAndProducers(t *testing.T) {
	unsetEnv(t, "PG_INIT_USER", "PG_INIT_PASSWORD", "PG_PRODUCER_ROLES")

	cfg, apply, err := loadInitConfig([]string{"--apply", "--pg-producer-roles", "app_one, app_two"}, io.Discard)
	if err != nil {
		t.Fatalf("load init config: %v", err)
	}
	if !apply {
		t.Fatal("expected apply to be true")
	}
	if !reflect.DeepEqual(cfg.PGProducerRoles, []string{"app_one", "app_two"}) {
		t.Fatalf("unexpected producer roles: %#v", cfg.PGProducerRoles)
	}
}

func TestInitPrintRedactsPassword(t *testing.T) {
	cfg := baselineInitConfig()
	cfg.PGInitUser = "admin"

	printed := initScript(cfg, true)
	assertContains(t, printed, "PASSWORD NULL")
	assertContains(t, printed, "Configure authentication for the run role separately")
	assertNotContains(t, printed, "s3cret")

	applied := initScript(cfg, false)
	assertContains(t, applied, `CREATE ROLE "outboxer" LOGIN PASSWORD 's3cret'`)
	assertNotContains(t, applied, "PASSWORD NULL")
}


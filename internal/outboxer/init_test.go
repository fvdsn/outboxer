func baselineInitConfig() appConfig {
	return appConfig{
		EventTable:       "events",
		EventID:          "id",
		EventPayload:     "payload",
		EventTarget:      "target",
		EventDestination: "destination",
		EventTimestamp:   "timestamp",
		EventOptions:     "options",
		PGUser:           "outboxer",
		PGPassword:       "s3cret",
		PGSchema:         "public",
		NotifyChannel:    "outboxer_events",
	}
}

func initScript(cfg appConfig, redactSecrets bool) string {
	return renderInitScript(buildInitStatements(cfg, redactSecrets))
}

func assertContains(t *testing.T, script, substr string) {
	t.Helper()
	if !strings.Contains(script, substr) {
		t.Fatalf("expected script to contain %q\n--- script ---\n%s", substr, script)
	}
}

func assertNotContains(t *testing.T, script, substr string) {
	t.Helper()
	if strings.Contains(script, substr) {
		t.Fatalf("expected script NOT to contain %q\n--- script ---\n%s", substr, script)
	}
}

func TestInitScriptCreatesAllConfiguredColumns(t *testing.T) {
	script := initScript(baselineInitConfig(), true)

	assertContains(t, script, `CREATE SCHEMA IF NOT EXISTS "public"`)
	assertContains(t, script, `CREATE TABLE IF NOT EXISTS "public"."events"`)
	assertContains(t, script, `"id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY`)
	assertContains(t, script, `"payload" text NOT NULL`)
	assertContains(t, script, `"target" text`)
	assertContains(t, script, `"destination" text`)
	assertContains(t, script, `"timestamp" timestamptz`)
	assertContains(t, script, `"options" jsonb`)
	assertContains(t, script, `ALTER TABLE "public"."events" SET (`)
	assertContains(t, script, `autovacuum_vacuum_scale_factor = 0.01`)
	assertContains(t, script, `autovacuum_analyze_scale_factor = 0.02`)
	assertContains(t, script, `autovacuum_vacuum_threshold = 1000`)
	assertContains(t, script, `autovacuum_analyze_threshold = 1000`)
}

func TestInitScriptQualifiesCustomSchema(t *testing.T) {
	cfg := baselineInitConfig()
	cfg.PGSchema = "application data"
	cfg.PGInitUser = "admin"
	cfg.PGProducerRoles = []string{"producer"}
	cfg.DLQTable = "dead_letters"
	cfg.PollInterval = time.Second

	script := initScript(cfg, true)

	assertContains(t, script, `CREATE SCHEMA IF NOT EXISTS "application data"`)
	assertContains(t, script, `CREATE TABLE IF NOT EXISTS "application data"."events"`)
	assertContains(t, script, `ALTER TABLE "application data"."events" SET (`)
	assertContains(t, script, `CREATE TABLE IF NOT EXISTS "application data"."dead_letters"`)
	assertContains(t, script, `CREATE OR REPLACE FUNCTION "application data"."outboxer_notify"()`)
	assertContains(t, script, `AFTER INSERT ON "application data"."events"`)
	assertContains(t, script, `EXECUTE FUNCTION "application data"."outboxer_notify"`)
	assertContains(t, script, `GRANT USAGE ON SCHEMA "application data" TO "outboxer"`)
	assertContains(t, script, `GRANT USAGE ON SCHEMA "application data" TO "producer"`)
}

